package ch.joth.games.pocketsimon.game.code;

/**
 * Enum representing the sound modes available in the game.
 * The sound modes are either SOUND_ON or SOUND_OFF.
 */
public enum eSoundMode {
    /**
     * Represents the sound mode being on. In this mode, sounds are played.
     */
    SOUND_ON,

    /**
     * Represents the sound mode being off. In this mode, no sounds are played.
     */
    SOUND_OFF
}